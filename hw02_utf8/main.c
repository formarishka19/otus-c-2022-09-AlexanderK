void checkArgs(int *argc, char* argv[]) {
    if (*argc != 4) {
        printf("\n\n --- ! ОШИБКА ЗАПУСКА ----\n\n"
        "Программа принимает на вход только три аргумента - путь до исходного файла, путь до результирующего файла и кодировку исходного файла\n\n"
        "------ Синтаксис ------\n"
        "%s <source filepath> <output filepath> <source file encoding>\n"
        "-----------------------\n\n"
        "Пример запуска\n"
        "%s ./files/koi8.txt ./output.txt koi8|iso-8859-5|cp1251\n\n"
        "--- Повторите запуск правильно ---\n\n", argv[0], argv[0]);
        exit (1);
    }
}

size_t getFileSize(FILE *fp) {
    struct stat finfo;
    if (!fstat(fileno(fp), &finfo)) {
        return finfo.st_size;
    }
    else {
        printf("Cannot get file info.\n");
        exit (1);
    }
}

int findIndex(const unsigned short *array, int *size, unsigned short target) 
{
    int i = 0;
    while(( i < *size) && (array[i] != target)) i++;
    return (i < *size) ? (i) : (-1);
}

int main(int argc, char* argv[]) {
    checkArgs(&argc, argv);
    (void)argc;

    unsigned short* card;
    unsigned short* map;
    int map_size;

    if (strcmp(argv[3], "koi8") == 0) {
        map_size = koi8_map_size;
        card = koi_card;
        map = koi_map;
    } else if (strcmp(argv[3], "iso-8859-5") == 0) {
        map_size = iso_map_size;
        card = iso_card;
        map = iso_map;
    } else if (strcmp(argv[3], "cp1251") == 0) {
        map_size = win_map_size;
        card = win_card;
        map = win_map;
    } 
    else {
        printf("Unknown encoding %s, choose available: koi8|iso-8859-5|cp1251\n", argv[2]);
        exit (1);
    }

    FILE *fpin, *fpout;
    char* CUR_FILE_NAME = argv[1];
    char* OUT_FILE_NAME = argv[2];
    unsigned short ucode;

    if ((fpin=fopen(CUR_FILE_NAME, "r") ) == NULL) {
        printf("Error opening file %s\n", CUR_FILE_NAME);
        exit (1);
    }
    if ((fpout=fopen(OUT_FILE_NAME, "wb") ) == NULL) {
        printf("Error creating output file, check permissions or path %s\n", OUT_FILE_NAME);
        exit (1);
    }

    size_t fsize = getFileSize(fpin);
    unsigned short ch;
    int map_index = -1;
    
    for (long unsigned int i = 0; i < fsize; i++) {
        ch = fgetc(fpin);
        if (ch < 0x7F) {
            fputc(ch & 0x7F, fpout);
        } 
        else {
            map_index = findIndex(card, &map_size, ch);
            if (map_index != -1) {
                ucode = map[map_index];
                if (ucode >= 0x80 && ucode <= 0x7FF) {
                    fputc(((ucode >> 6) & 0x1F) | 0xC0, fpout);
                    fputc(((ucode >> 0) & 0x3F) | 0x80, fpout);
                }
                // else if (ucode >= 0x800 && ucode <= 0xFFFF) {
                //     printf("Error, check if file encoding is correct\n");
                //     break;
                // }
                else {
                    printf("Error, check if file encoding is correct\n");
                    fclose(fpin);
                    fclose(fpout);
                    remove(OUT_FILE_NAME);
                    exit(1);
                    break;
                }
            }
            else {
                printf("Error, check if file encoding is correct\n");
                fclose(fpin);
                fclose(fpout);
                remove(OUT_FILE_NAME);
                exit(1);
                break;
            }
        }
    }
    fclose(fpin);
    fclose(fpout);
    printf("File %s successfuly converted to %s", CUR_FILE_NAME, OUT_FILE_NAME);
    return 0;
}
